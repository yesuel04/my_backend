package project;

public class Meal {
    int year;
    int month;
    int day;
    String yoil;
    String store;
    String menu;
    int price;
    
    public Meal(int year, int month, int day, String yoil, String store, String menu, int price) {
        this.year = year;
        this.month = month;
        this.day = day;
        this.yoil = yoil;
        this.store = store;
        this.menu = menu;
        this.price = price;
    }
}
