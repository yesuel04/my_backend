package a1114;

public class Switch2 {
    public static void main(String[] args) {
        char grade = 'B';
        switch(grade){
            case 'A':
            case 'a':
                System.out.println("Aa 입니다");
                break;
            case 'B':
            case 'b':
                System.out.println("Bb 입니다");
                break;
            case 'C':
            case 'c':
                System.out.println("Cc 입니다");
                break;
        }
    }
}
