package a1114;

public class Switch3 {
    public static void main(String[] args) {
        char grade = 'B';
        switch(grade){
            case 'A','a'-> {
                System.out.println("Aa 입니다");
                break;
            }
            case 'B','b'->{
                System.out.println("Bb 입니다");
                break;
            }
            case 'C','c'->{
                System.out.println("Cc 입니다");
                break;
            }
        }
    }
}
